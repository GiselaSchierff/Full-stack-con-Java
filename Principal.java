        boolean estaElNombre = control.buscarNombre(nombreBuscar); 
        if (estaElNombre == true){
            txtResultado.setText("El nombre que buscabas fue encontrado");
        }
        else { 
            txtResultado.setText("El nombre que buscabas no fue encontrado");
        }
